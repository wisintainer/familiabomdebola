package bomdebola.bomdebola.model;

public class Status {
	private Integer rodada_atual;
	private Integer status_mercado;

	public Integer getRodada_atual() {
		return rodada_atual;
	}

	public void setRodada_atual(Integer rodada_atual) {
		this.rodada_atual = rodada_atual;
	}

	public Integer getStatus_mercado() {
		return status_mercado;
	}

	public void setStatus_mercado(Integer status_mercado) {
		this.status_mercado = status_mercado;
	}

}
